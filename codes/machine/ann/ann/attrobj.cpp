//	class GridObj
////////////////////////////////////////////////////////////////////////////
PositionObj::PositionObj()
{
	x = y = z = 0;
}

////////////////////////////////////////////////////////////////////////////
//	class SharedObj
////////////////////////////////////////////////////////////////////////////
SharedObj::SharedObj()
{
	m_use_count = 0;
}

ann_int SharedObj::addref()
{
	++m_use_count;
	return m_use_count;
}

ann_int SharedObj::release()
{
	--m_use_count;
	return m_use_count;
}

